
#Assignment Operator
 
x = 10
y = 10
 
# += Add Assign
x = x + 2
y += 2
print("x = ", x, " y = ", y)
 
# -= Subtract Assign
x = x - 2
y -= 2
print("x = ", x, " y = ", y)
 
# *= Multiply Assign
x = x * 2
y *= 2
print("x = ", x, " y = ", y)
 
# /= Divide Assign
x = x / 2
y /= 2
print("x = ", x, " y = ", y)
 
