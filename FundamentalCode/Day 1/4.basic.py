#Initialization
a = [5, 10, 15, 20, 25, 30, 35, 40]
#    ^  ^   ^   ^   ^   ^   ^   ^
#    0  1   2   3   4   5   6   7
 
print("a[2] = ", a[2])
#Output a[2] => 15
 
print("a[0:3] = ", a[0:3])
#Output a[0:3] => [5, 10, 15] 
 
 
print("a[5:] = ", a[5:])
#Output => a[5:] = [30, 35, 40] 
 
 
print("a[:3] = ", a[:3])
#Output => a[:3] = [5, 10, 15]
