#Logical Operator
isTRUE = True
isFALSE = False
 
# Using and logical operator
print('isTRUE and isTRUE => ', isTRUE and isTRUE)
print('isTRUE and isFALSE => ', isTRUE and isFALSE)
print('isFALSE and isTRUE => ', isFALSE and isTRUE)
print('isFALSE and isFALSE => ', isFALSE and isFALSE)
 
print("====================================")
# Using or logical operator
print('isTRUE or isTRUE => ', isTRUE or isTRUE)
print('isTRUE or isFALSE => ', isTRUE or isFALSE)
print('isFALSE or isTRUE => ', isFALSE or isTRUE)
print('isFALSE or isFALSE => ', isFALSE or isFALSE)
 
print("====================================")
