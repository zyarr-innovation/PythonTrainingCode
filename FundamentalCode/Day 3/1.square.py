
import turtle
pen = turtle.Pen()
 
pen.forward(100) # This makes our first forward (pointing north)
pen.left(90) # Then the turtle turns left 90 degrees
pen.forward(100) # This makes our second forward (pointing west)
pen.left(90) # Then the turtle turns left 90 degrees
pen.forward(100) # This makes our third forward (pointing south)
pen.left(90) # Then the turtle turns left 90 degrees
pen.forward(100) # This makes our fourth forward (pointing east)
 
